def handle(bot, message):
    text = getattr(message, "text", "").strip().lower()
    if not text.startswith(TRIGGER):
        return

    name = get_name_safe(message)
    parts = text.split()

    if len(parts) >= 2 and parts[1] == "–ø–æ—Ä—á–∞":
        target_name = get_name_safe(message.reply_to_message) if message.reply_to_message else name
        bot.reply_to(message, generate_curse(target_name))
        return

    count = 3
    if len(parts) >= 2:
        try:
            n = int(parts[1])
            if n in [3,5,7,10]:
                count = n
        except:
            pass

    bot.reply_to(message, generate_prediction(name, count))

