def get_name_safe(message):
    user = getattr(message, "from_user", None)
    if not user and hasattr(message, "reply_to_message"):
        user = getattr(message.reply_to_message, "from_user", None)
    if user:
        first = getattr(user, "first_name", None) or ""
        last = getattr(user, "last_name", None) or ""
        full_name = f"{first} {last}".strip()
        return full_name if full_name else "–¥—Ä—É–≥"
    return "–¥—Ä—É–≥"

