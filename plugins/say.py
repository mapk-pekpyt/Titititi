def handle(bot, message):
    text = getattr(message, "text", "").strip()
    if not text.lower().startswith(TRIGGER):
        return

    name = get_name(message) or getattr(getattr(message, "from_user", None), "first_name", "–¥—Ä—É–≥")

    parts = text.split()

    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –ø–æ—Ä—á—É
    if len(parts) >= 2 and parts[1].lower() == "–ø–æ—Ä—á–∞":
        if hasattr(message, "reply_to_message") and message.reply_to_message:
            target_name = get_name(message.reply_to_message) or getattr(getattr(message.reply_to_message, "from_user", None), "first_name", "–¥—Ä—É–≥")
        else:
            target_name = name
        bot.reply_to(message, generate_curse(target_name))
        return

    # –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –∫–∞—Ä—Ç
    count = 3
    if len(parts) >= 2:
        try:
            n = int(parts[1])
            if n in [3, 5, 7, 10]:
                count = n
        except:
            pass

    bot.reply_to(message, generate_prediction(name, count))

