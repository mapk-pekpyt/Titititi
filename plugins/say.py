def handle(bot, message):
    text = (message.text or "").strip().lower()
    if not text.startswith(TRIGGER):
        return

    user = getattr(message, "from_user", None)

    parts = text.split()

    # –ü–æ—Ä—á–∞
    if len(parts) >= 2 and parts[1] == "–ø–æ—Ä—á–∞":
        target_user = getattr(message.reply_to_message, "from_user", None) or user
        bot.reply_to(message, generate_curse(target_user))
        return

    # –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –∫–∞—Ä—Ç
    count = 3
    if len(parts) >= 2:
        try:
            n = int(parts[1])
            if n in [3,5,7,10]:
                count = n
        except:
            pass

    bot.reply_to(message, generate_prediction(user, count))

