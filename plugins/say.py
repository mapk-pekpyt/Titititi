def generate_prediction(name, count=3):
    all_cards = CARDS[:]
    selected_cards = random.sample(all_cards, min(count, len(all_cards)))
    messages = [f"üîÆ –Ø –≤–∏–∂—É, {name}‚Ä¶\n"]

    for card in selected_cards:
        meaning = random.choice(CARD_MEANINGS.get(card, ["–¢–∞–∏–Ω—Å—Ç–≤–µ–Ω–Ω–∞—è –∫–∞—Ä—Ç–∞ üåå"]))
        messages.append(f"üåë {card}: {meaning}")

    messages.append(random.choice(FINAL_PREDICTIONS))
    return "\n".join(messages)

