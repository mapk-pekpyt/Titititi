def handle(bot, message):
    if not getattr(message, "text", None):
        return
    text = message.text.strip().lower()
    if not text.startswith(TRIGGER):
        return

    user = getattr(message, "from_user", None)
    name = get_name(user) if user else None
    name = name or getattr(user, "first_name", "–¥—Ä—É–≥")

    parts = text.split()

    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –ø–æ—Ä—á—É
    if len(parts) >= 2 and parts[1] == "–ø–æ—Ä—á–∞":
        reply_user = getattr(getattr(message, "reply_to_message", None), "from_user", None)
        target_name = get_name(reply_user) if reply_user else None
        target_name = target_name or getattr(reply_user, "first_name", "–¥—Ä—É–≥")
        bot.reply_to(message, generate_curse(target_name))
        return

    # –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –∫–∞—Ä—Ç
    count = 3
    if len(parts) >= 2:
        try:
            n = int(parts[1])
            if n in [3, 5, 7, 10]:
                count = n
        except:
            pass

    bot.reply_to(message, generate_prediction(name, count))

