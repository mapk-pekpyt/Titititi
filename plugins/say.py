def random_hourly_vision(bot, chat_id):
    def run():
        while True:
            time.sleep(3600)
            try:
                chat_members = bot.get_chat_administrators(chat_id)
                if not chat_members:
                    continue
                member = random.choice(chat_members).user
                message = generate_prediction(member, 3)
                bot.send_message(chat_id, f"üîÆ –í–Ω–µ–∑–∞–ø–Ω–æ–µ –≤–∏–¥–µ–Ω–∏–µ –¥–ª—è {get_name(member)}:\n{message}")
            except Exception as e:
                print("–û—à–∏–±–∫–∞ —Å–ª—É—á–∞–π–Ω–æ–≥–æ –≤–∏–¥–µ–Ω–∏—è:", e)
    threading.Thread(target=run, daemon=True).start()